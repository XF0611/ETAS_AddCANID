boolean FBL_ImageM_GetSyncCondition(void)
{
    boolean Condition = FALSE;
    Dcm_SesCtrlType ActiveSession;
    (void)Dcm_GetSesCtrlType(&ActiveSession);
    if ((ActiveSession == DCM_SESSION_PROGRAMMING) || (ActiveSession == DCM_SESSION_BACKGROUND_DOWNLOAD))
    {
        Condition = FALSE;
    }
    else
    {
        Condition = TRUE;
    }
    return Condition;
}

